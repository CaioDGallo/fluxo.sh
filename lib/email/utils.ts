/**
 * Escapes HTML special characters to prevent XSS in email templates.
 * Converts &, <, >, ", and ' to their HTML entity equivalents.
 */
export function escapeHtml(text: string | null | undefined): string {
  if (!text) return '';
  return text
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&#039;');
}
